// shorthand assignment operators demo
class Welcome
{
	public static void main(String args[])
	{
		 
		int x=5,y=8;
		 
		System.out.println(x+=y); // x=x+y => x=5+8 => x=13
		System.out.println(y-=6); // y=y-6 => y=8-6 => y=2
		System.out.println(x*=10); // x=x*10 => x=13*10 => x=130
		System.out.println(y/=2); // y=y/2 => y=2/2 => y=1
		System.out.println(x%=3); // x=x%3 => x=130%3 => x=1
		 

		


	}
}

