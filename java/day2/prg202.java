// if else
class Demo
{
	public static void main(String [] args)
	{
		int x=14;
		System.out.println("before condition");
		if(x%3==0)
		{
			System.out.println("number is divisible by 3");
		}
		else
		{
			System.out.println("number is not divisible by 3");
		}
		System.out.println("after condition");
	}
}
